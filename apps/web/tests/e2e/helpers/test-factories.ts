// Ticket
// ────────────────────────────────────────────────────────────

export type TestTicket = { id: string }

export const createTestTicket = async (
  orgId: string,
  userId: string,
  projectId?: string,
): Promise<TestTicket> => {
  const admin = getAdminClient()

  // Note: sequence_number is auto-generated by a DB trigger
  const { data: ticket, error } = await admin
    .from('tickets')
    .insert({
      org_id: orgId,
      title: `E2E Test Ticket ${Date.now()}`,
      description: 'Created by E2E test factory',
      type: 'task',
      status: 'backlog',
      priority: 'medium',
      reporter_id: userId,
      project_id: projectId ?? null,
    })
    .select('id')
    .single()

  if (error || !ticket) {
    throw new Error(`Failed to create test ticket: ${error?.message}`)
  }

  return { id: ticket.id }
}

