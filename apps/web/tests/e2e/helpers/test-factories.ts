// ────────────────────────────────────────────────────────────
// Cleanup
// ────────────────────────────────────────────────────────────

/**
 * Delete a test org and all its cascading data.
 * Use when cleanup needs to happen independently of user deletion
 * (e.g., when multiple orgs are created for the same user).
 */
export const cleanupTestOrg = async (orgId: string): Promise<void> => {
  const admin = getAdminClient()

  // Delete audit_log entries first (FK constraint blocks org deletion)
  await admin.from('audit_log').delete().eq('org_id', orgId)

  const { error } = await admin.from('organizations').delete().eq('id', orgId)

  if (error) {
    console.error(`Failed to cleanup test org ${orgId}: ${error.message}`)
  }
}
